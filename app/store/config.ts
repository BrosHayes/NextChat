export function limitNumber(
  x: number,
  min: number,
  max: number,
  defaultValue: number,
) {
  if (isNaN(x)) {
    return defaultValue;
  }

  return Math.min(max, Math.max(min, x));
}

export function getContextWindowTokens(config?: Partial<ModelConfig>) {
  const fallback =
    config?.max_tokens ?? DEFAULT_CONFIG.modelConfig.contextWindowTokens;

  return limitNumber(
    Number(config?.contextWindowTokens ?? fallback),
    0,
    MODEL_TOKEN_LIMIT_MAX,
    fallback,
  );
}

export const TTSConfigValidator = {
  engine(x: string) {
    return x as TTSEngineType;
  },
  model(x: string) {
    return x as TTSModelType;
  },
  voice(x: string) {
    return x as TTSVoiceType;
  },
  speed(x: number) {
    return limitNumber(x, 0.25, 4.0, 1.0);
  },
};

export const ModalConfigValidator = {
  model(x: string) {
    return x as ModelType;
  },
  contextWindowTokens(x: number) {
    return limitNumber(
      x,
      0,
      MODEL_TOKEN_LIMIT_MAX,
      DEFAULT_CONFIG.modelConfig.contextWindowTokens,
    );
  },
  max_tokens(x: number) {
    return limitNumber(x, 0, MODEL_TOKEN_LIMIT_MAX, 1024);
  },
  presence_penalty(x: number) {
    return limitNumber(x, -2, 2, 0);
  },
  frequency_penalty(x: number) {
    return limitNumber(x, -2, 2, 0);
  },
  temperature(x: number) {
    return limitNumber(x, 0, 2, 1);
  },
  top_p(x: number) {
    return limitNumber(x, 0, 1, 1);
  },
};

export const useAppConfig = createPersistStore(
  { ...DEFAULT_CONFIG },
  (set, get) => ({
    reset() {
      set(() => ({ ...DEFAULT_CONFIG }));
    },

    mergeModels(newModels: LLMModel[]) {
      if (!newModels || newModels.length === 0) {
        return;
      }

      const oldModels = get().models;
      const modelMap: Record<string, LLMModel> = {};

      for (const model of oldModels) {
        model.available = false;
        modelMap[`${model.name}@${model?.provider?.id}`] = model;
      }

      for (const model of newModels) {
        model.available = true;
        modelMap[`${model.name}@${model?.provider?.id}`] = model;
      }

      set(() => ({
        models: Object.values(modelMap),
      }));
    },

    allModels() {},
  }),
  {
    name: StoreKey.Config,
    version: 4.4,

    merge(persistedState, currentState) {
      const state = persistedState as ChatConfig | undefined;
      if (!state) return { ...currentState };
      const models = currentState.models.slice();
      state.models.forEach((pModel) => {
        const idx = models.findIndex(
          (v) => v.name === pModel.name && v.provider === pModel.provider,
        );
        if (idx !== -1) models[idx] = pModel;
        else models.push(pModel);
      });
      return { ...currentState, ...state, models: models };
    },

    migrate(persistedState, version) {
      const state = persistedState as ChatConfig;

      if (version < 3.4) {
        state.modelConfig.sendMemory = true;
        state.modelConfig.historyMessageCount = 4;
        state.modelConfig.compressMessageLengthThreshold = 1000;
        state.modelConfig.frequency_penalty = 0;
        state.modelConfig.top_p = 1;
        state.modelConfig.template = DEFAULT_INPUT_TEMPLATE;
        state.dontShowMaskSplashScreen = false;
        state.hideBuiltinMasks = false;
      }

      if (version < 3.5) {
        state.customModels = "claude,claude-100k";
      }

      if (version < 3.6) {
        state.modelConfig.enableInjectSystemPrompts = true;
      }

      if (version < 3.7) {
        state.enableAutoGenerateTitle = true;
      }

      if (version < 3.8) {
        state.lastUpdate = Date.now();
      }

      if (version < 3.9) {
        state.modelConfig.template =
          state.modelConfig.template !== DEFAULT_INPUT_TEMPLATE
            ? state.modelConfig.template
            : config?.template ?? DEFAULT_INPUT_TEMPLATE;
      }

      if (version < 4.1) {
        state.modelConfig.compressModel =
          DEFAULT_CONFIG.modelConfig.compressModel;
        state.modelConfig.compressProviderName =
          DEFAULT_CONFIG.modelConfig.compressProviderName;
      }

      if (version < 4.2) {
        state.modelConfig.max_tokens = DEFAULT_CONFIG.modelConfig.max_tokens;
        state.modelConfig.historyMessageCount =
          DEFAULT_CONFIG.modelConfig.historyMessageCount;
        state.modelConfig.compressMessageLengthThreshold =
          DEFAULT_CONFIG.modelConfig.compressMessageLengthThreshold;
      }

      if (version < 4.3) {
        const ttsConfig = state.ttsConfig;
        const usesLegacyDefaultTTSConfig =
          !ttsConfig ||
          ((ttsConfig.enable ?? false) === false &&
            (ttsConfig.engine ?? DEFAULT_TTS_ENGINE) === DEFAULT_TTS_ENGINE &&
            (ttsConfig.model ?? DEFAULT_TTS_MODEL) === DEFAULT_TTS_MODEL &&
            (ttsConfig.voice ?? DEFAULT_OPENAI_TTS_VOICE) ===
              DEFAULT_OPENAI_TTS_VOICE &&
            (ttsConfig.speed ?? 1.0) === 1.0);

        if (usesLegacyDefaultTTSConfig) {
          state.ttsConfig = { ...DEFAULT_CONFIG.ttsConfig };
        }
      }

      if (version < 4.4) {
        state.modelConfig.contextWindowTokens =
          state.modelConfig.contextWindowTokens ??
          state.modelConfig.max_tokens ??
          DEFAULT_CONFIG.modelConfig.contextWindowTokens;
      }

      return state as any;
    },
  },
);
